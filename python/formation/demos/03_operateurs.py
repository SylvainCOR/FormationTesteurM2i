
#---------Operateurs arithmetiques----------

print(5 / 3)    # div classique
print(5 // 3)   # div entiere
print(5 % 3)    # modulo (reste)

print(2 ** 10)  # puissance 10

print(pow(2,10))    # carré de base (equivalent **)


import math

#---------Module math-------------

print(math.pow(2,10))   # carré du module math
print(math.pi)          # Pi

var = math.pi
print(math.ceil(var))   # arrondi sup
print(math.floor(var))  # arrondi inf


#---------Modif variable avec opéranteur-------------

var = 50
print(var)
var *= 5    # var = var * 5
print(var)
var //= 3   # var = var // 3
print(var)


